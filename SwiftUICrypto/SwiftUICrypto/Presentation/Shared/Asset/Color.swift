import SwiftUICore

extension Color {
    static let theme = ColorThem()
}

struct ColorThem {
    let accentColor = Color("AccentColor")
    let background = Color("Background")
    let greenColor = Color("GreenColor")
    let redColor = Color("RedColor")
    let secondaryTextColor = Color("SecondaryTextColor")
}
